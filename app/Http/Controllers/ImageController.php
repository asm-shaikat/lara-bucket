        if ($disk->exists('images/' . $filename)) {
            $file = $disk->get('images/' . $filename);
            $type = $disk->mimeType('images/' . $filename);
            return response($file, 200)
                    ->header('Content-Type', $type);
        }

        abort(404);
    }

